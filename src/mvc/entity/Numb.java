package mvc.entity;

public class Numb {
	
	private String total;
	private String onnum;
	private String offnum;
	private String hnum;
	private String nnum;
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getOnnum() {
		return onnum;
	}
	public void setOnnum(String onnum) {
		this.onnum = onnum;
	}
	public String getOffnum() {
		return offnum;
	}
	public void setOffnum(String offnum) {
		this.offnum = offnum;
	}
	public String getHnum() {
		return hnum;
	}
	public void setHnum(String hnum) {
		this.hnum = hnum;
	}
	public String getNnum() {
		return nnum;
	}
	public void setNnum(String nnum) {
		this.nnum = nnum;
	}
	
	

}
